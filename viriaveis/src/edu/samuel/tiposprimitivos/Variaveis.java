package edu.samuel.tiposprimitivos;

public class Variaveis {

    public static void main(String[] args){ 
        // variaveis seguem no estilo camelCase e precisao de seu tipo e nome para ser criadas ! 
        int valor = 0 ; 
        // ou podemos adcionar um valor futuaramente e so criar elas 
        int v  = 1;
        // No formato do JAVA nao podemos mudar seu tipo no meio do codigo 
        // float v ; só no momento que instaciamos ela ! 

        final float  VALORFINAL =  1 ; 
        // Uma constante nao pode ter mudado ou acrecentado depois de criada !
        // e seu nome é sempre MAIUSCULO  
        System.out.println("Apenas para chamar " + valor + v + VALORFINAL);
    }
    
}
